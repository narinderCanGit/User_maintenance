    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (txtName.getText().trim().isEmpty()) {
            Utility.showMsg(this, "Please enter name ", 'w');
        } else if (txtUsername.getText().trim().isEmpty()) {
            Utility.showMsg(this, "Please enter Username ", 'w');
        } else if (String.valueOf(txtPassword.getPassword()).isEmpty()) {
            Utility.showMsg(this, "Please enter password ", 'w');
        } else if (txtContactNo.getText().trim().isEmpty()) {
            Utility.showMsg(this, "Please enter ContactNo ", 'w');

        } else if (txtEmailId.getText().trim().isEmpty()) {
            Utility.showMsg(this, "Please enter EmailId ", 'w');
        } else if (txtDOB.getText().trim().isEmpty()) {
            Utility.showMsg(this, "Please enter DOB ", 'w');
        } else {
            UserBean objbean = new UserBean();
            objbean.setUsername(txtUsername.getText().trim());
            objbean.setPassword(String.valueOf(txtPassword.getPassword()));
            objbean.setName(txtName.getText().trim());
            objbean.setDob(txtDOB.getText().trim());
            objbean.setEmailid(txtEmailId.getText().trim());
            objbean.setContactno(txtContactNo.getText().trim());
            objbean.setAddress(taAddress.getText().trim());
            objbean.setUsertype(String.valueOf(ddlUserType.getSelectedItem()));
            if (rbActive.isSelected()) {
                objbean.setUserstatus(true);
            } else {
                objbean.setUserstatus(false);
            }
            if (addupdateflag == 1) {
                 objbean.setUserid(al.get(al.size() - 1).getUserid() + 1);   //to assign userid(last)
                
                 if (!photopath.equals("")) {
                    Utility.saveImage(photopath, "images/" + objbean.getUserid() + ".jpg");  //uploadPath, savePath
                }

                if (ManageUserServices.addUser(objbean)) {
                    Utility.showMsg(this, "Record added", 'i');
                } else {
                    Utility.showMsg(this, "Record not added", 'w');
                }
            } else if (addupdateflag == 0) {
                objbean.setUserid(al.get(count).getUserid()); //current userid

                if (!photopath.equals("")) {
                    Utility.saveImage(photopath, "images/" + objbean.getUserid() + ".jpg"); 
                }

                if (ManageUserServices.updateById(objbean)) {
                    Utility.showMsg(this, "Record updated", 'i');
                } else {
                    Utility.showMsg(this, "Record not updated", 'w');
                }
            }
            afterSave();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

