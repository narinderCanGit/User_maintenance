    private void showRecord() {
        UserBean objbean = al.get(count);       //used to get data of any random user that depends upon count
        txtUsername.setText(objbean.getUsername());
        txtPassword.setText(objbean.getPassword());
        txtName.setText(objbean.getName());
        txtContactNo.setText(objbean.getContactno());
        ddlUserType.setSelectedItem(objbean.getUsertype());
        taAddress.setText(objbean.getAddress());
        txtDOB.setText(objbean.getDob());
        txtEmailId.setText(objbean.getEmailid());
        if (objbean.isUserstatus()) {
            rbActive.setSelected(true);
        } else {
            rbInActive.setSelected(true);
        }

        File fpath = new File("images/" + objbean.getUserid() + ".jpg");   
        if (fpath.exists()) {
            Utility.showImage(fpath.getPath(), lblProfilePhoto);
        } else {
            Utility.showImage("images/default.jpg", lblProfilePhoto);   //Default profile pic
        }

        btnFirst.setEnabled(true);
        btnPrevious.setEnabled(true);
        btnNext.setEnabled(true);        //disable or enable buttons
        btnLast.setEnabled(true);
        btnSave.setEnabled(false);
        btnUpload.setEnabled(false);
        btnCheckAvailibility.setEnabled(false);

        if (count == 0) {         //first record
            btnFirst.setEnabled(false);
            btnPrevious.setEnabled(false);
        }

        if (count == al.size() - 1) {         //last record

            btnNext.setEnabled(false);
            btnLast.setEnabled(false);
        }

        enableAll(false);       //Method used below
        addupdateflag=-1;
        
        btnEdit.setEnabled(true);
        btnAdd.setEnabled(true);
    }
    
     public void enableAll(boolean flag) {
        txtUsername.setEditable(flag);
        txtPassword.setEditable(flag);
        txtName.setEditable(flag);
        txtContactNo.setEditable(flag);
        txtEmailId.setEditable(flag);
        taAddress.setEditable(flag);
        txtDOB.setEditable(flag);
        ddlUserType.setEnabled(flag);
        rbActive.setEnabled(flag);
        rbInActive.setEnabled(flag);

    }

    private void afterSave() {
        al = ManageUserServices.getAllUsers();
        if (addupdateflag == 1) {
            count = al.size() - 1;
        }
        showRecord();
        btnAdd.setEnabled(true);
        btnEdit.setEnabled(true);
        addupdateflag = -1;
        
      
    }

   

