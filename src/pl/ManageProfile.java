    private void btnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadActionPerformed
        JFileChooser jfc = new JFileChooser();     //to choose file when we click upload
        int i = jfc.showOpenDialog(this);         //this denotes popup window on this window to choose file
        photopath = jfc.getSelectedFile().getPath();
        if (i == JFileChooser.APPROVE_OPTION) {
            System.out.println(jfc.getSelectedFile());   //display the path of img
            Utility.showImage(photopath, lblProfilePhoto);
        }
    }//GEN-LAST:event_btnUploadActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (txtName.getText().trim().isEmpty()) {
            Utility.showMsg(this, "Please enter name ", 'w');
        } else if (String.valueOf(txtPassword.getPassword()).isEmpty()) {
            Utility.showMsg(this, "Please enter password ", 'w');
        } else {
            UserBean obj = new UserBean();

            obj.setPassword(String.valueOf(txtPassword.getPassword()));
            obj.setName(txtName.getText().trim());
            obj.setDob(txtDOB.getText().trim());

            obj.setContactno(txtContactNo.getText().trim());
            obj.setAddress(taAddress.getText().trim());

            if (flag == true) {
                obj.setUserid(uid); //current userid

                if (!photopath.equals("")) {
                    Utility.saveImage(photopath, "images/" + uid + ".jpg");
                }

                if (ManageProfileServices.updateById(obj)) {
                    Utility.showMsg(this, "Record updated", 'i');
                } else {
                    Utility.showMsg(this, "Record not updated", 'w');
                }
            }
            btnEdit.setEnabled(true);
            btnSave.setEnabled(false);
            btnUpload.setEnabled(false);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

