    private void showRecord() {

        txtPassword.setText(objbean.getPassword());
        txtName.setText(objbean.getName());
        txtContactNo.setText(objbean.getContactno());
        txtUsername.setText(objbean.getUsername());
        taAddress.setText(objbean.getAddress());
        txtDOB.setText(objbean.getDob());

        File fpath = new File("images/" + uid + ".jpg");
        if (fpath.exists()) {
            Utility.showImage(fpath.getPath(), lblProfilePhoto);
        } else {
            Utility.showImage("images/default.jpg", lblProfilePhoto);   //Default profile pic
        }

        btnSave.setEnabled(false);
        btnEdit.setEnabled(true);
        btnUpload.setEnabled(false);

        enableAll(false);
        flag = false;

    }

